# sorting
c = [1,5,4,2,67,34]
c.sort()
print(c)

# reverse
c.reverse()
print(c)

# append = add the end of the document
c.append(8)
print(c)

# insert = add on the index number any value
c.insert(0,45)
print(c)

# pop = remove the value from index numbers
c.pop(0)
print(c)

# remove = remove the value of the numbers
c.remove(67)
print(c)
